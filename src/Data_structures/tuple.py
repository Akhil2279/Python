# indexing in tuple
colors = ("red", "green", "blue")

print("First color is:", colors[0])

#Access elements from tuple
numbers = (10, 20, 30, 40, 50)

print("First element:", numbers[0])
print("Last element:", numbers[-1])

#Length of tuple
numbers = (1, 2, 3, 4, 5)

print("Length of tuple:", len(numbers))

#Count occurrence of element in tuple

numbers = (1, 2, 3, 2, 4, 2)

count = numbers.count(2)
print("Count:", count)
