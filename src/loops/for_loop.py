# for loop to print numbers from 1 to 10

for i in range(1, 11):
    print(i)


# for loop to print even numbers from 2 to 20
numbers = [10, 20, 30, 40]
total = 0
for num in numbers:
    total = total + num

print("Total sum is:", total)

# Print elements of a list
fruits = ["apple", "banana", "cherry"]
for fruit in fruits:
    print(fruit)


# Count the number of elements in a list using for loop
numbers = [10, 20, 30, 40, 50]
count = 0

for num in numbers:
    count += 1

print("Count:", count)

# Print numbers from 10 to 1 using for loop
for i in range(10, 0, -1):
    print(i)
